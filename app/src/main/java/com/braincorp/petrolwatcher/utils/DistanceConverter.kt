package com.braincorp.petrolwatcher.utils

fun kilometresToMetres(kilometres: Float) = kilometres * 1000f

fun kilometresToMiles(kilometres: Float) = kilometres * 0.621371f

fun kilometresToYards(kilometres: Float) = kilometres * 1093.61f

fun metresToKilometres(metres: Float) = metres * 0.001f

fun metresToMiles(metres: Float) = metres * 0.000621371f

fun metresToYards(metres: Float) = metres * 1.09361f

fun yardsToKilometres(yards: Float) = yards * 0.0009144f

fun yardsToMetres(yards: Float) = yards * 0.9144f

fun yardsToMiles(yards: Float) = yards * 0.000568182f
